CREATE TABLE Nazioni(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    nome varchar(20) NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID)
);

CREATE TABLE Regioni(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Nazione int(6) NOT NULL,
    nome varchar(20) NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT NazioneRegione FOREIGN KEY (Nazione) REFERENCES Nazioni(ID)
);

CREATE TABLE Scuole(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Regione int(6) NOT NULL,
    nome varchar(20) NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT RegioneScuola FOREIGN KEY (Regione) REFERENCES Regioni(ID)
);

CREATE TABLE Squadre(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Sede int(6) NOT NULL,
    nome varchar(20) NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT SedeSquadra FOREIGN KEY (Sede) REFERENCES Scuole(ID)
);

CREATE TABLE Atleti(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Squadra int(6),
    Sede int(6) NOT NULL,
    nome varchar(20) NOT NULL,
    cognome varchar(20) NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT SquadraAtleta FOREIGN KEY (Squadra) REFERENCES Squadre(ID),
    CONSTRAINT SedeAtleta FOREIGN KEY (Sede) REFERENCES Scuole(ID)
);

CREATE TABLE Gara(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Sede int(6) NOT NULL,
    nome varchar(50) NOT NULL,
    fase ENUM("Scolastica", "Regionale", "Nazionale", "Internazionale") NOT NULL,
    data datetime NOT NULL,
    tipo ENUM("Individuale", "Squadra") NOT NULL,

    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT SedeGara FOREIGN KEY (Sede) REFERENCES Scuole(ID)
);

CREATE TABLE Partecipazioni(
    ID int(6) NOT NULL UNIQUE AUTO_INCREMENT,
    Atleta int(6),
    Squadra int(6),
    Gara int(6) NOT NULL,
    voto int(3),
    
    CONSTRAINT ChiavePrimaria PRIMARY KEY (ID),
    CONSTRAINT AtletaPartecipazione FOREIGN KEY (Atleta) REFERENCES Atleti(ID),
    CONSTRAINT SquadraPartecipazione FOREIGN KEY (Squadra) REFERENCES Squadre(ID)
);

